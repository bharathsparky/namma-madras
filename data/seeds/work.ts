export type WorkCategory =
  | 'labour_stand'          // Physical spots where contractors hire daily workers
  | 'market_labour'         // Wholesale market loading/unloading work
  | 'employment_exchange'   // Govt employment registration
  | 'welfare_scheme'        // Govt schemes — cash benefits for workers
  | 'wage_info'             // Minimum wage and rate reference
  | 'job_portal'            // Free online/offline job portals
  | 'domestic_work'         // Housekeeping / domestic worker connections

export type WorkType =
  | 'construction'          // Mason, helper, painter, plumber, electrician
  | 'loading_unloading'     // Manual loading, market porter
  | 'domestic'              // Cook, cleaner, caretaker
  | 'all_types'             // Mix of all types
  | 'film_shoots'           // Background/crowd work in cinema
  | 'govt_scheme'           // Govt benefit/registration
  | 'factory_industrial'    // Factory / warehouse

